#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	int		len_src;
	size_t	i;

	i = 0;
	len_src = ft_strlen(src);
	if (!dst || !src)
		return (0);
	if (size > 0)
	{
		while (src[i] && i < size - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (len_src);
}
